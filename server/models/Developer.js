const mongoose = require('mongoose');

const developerSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  }
});

const Developer = mongoose.model('Developer', developerSchema);

module.exports = Developer;
